const languageNames = {
  fr: "Francais",
  en: "English",
  es: "Espanol",
  de: "Deutsch",
};

const defaultLanguage = "fr";
const supportedLanguages = Object.keys(translations);

function pickInitialLanguage() {
  const savedLanguage = localStorage.getItem("musicfin-language");
  if (supportedLanguages.includes(savedLanguage)) return savedLanguage;

  const browserLanguage = navigator.language.slice(0, 2).toLowerCase();
  return supportedLanguages.includes(browserLanguage) ? browserLanguage : defaultLanguage;
}

function applyLanguage(language) {
  const dictionary = translations[language] || translations[defaultLanguage];
  document.documentElement.lang = language;
  document.title = language === "fr" ? "MusicFin" : `MusicFin - ${languageNames[language]}`;

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    if (dictionary[key]) node.textContent = dictionary[key];
  });

  document.querySelectorAll("[data-lang]").forEach((button) => {
    const isActive = button.dataset.lang === language;
    button.setAttribute("aria-pressed", String(isActive));
    button.setAttribute("aria-label", languageNames[button.dataset.lang]);
  });

  localStorage.setItem("musicfin-language", language);
}

document.querySelectorAll("[data-lang]").forEach((button) => {
  button.addEventListener("click", () => applyLanguage(button.dataset.lang));
});

applyLanguage(pickInitialLanguage());
